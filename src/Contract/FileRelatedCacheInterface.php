<?php

declare(strict_types=1);

namespace Vasoft\Joke\Contract;

use Vasoft\Joke\Cache\FileRelatedCache;

/**
 * Контракт для файлового кэша, привязанного к исходному файлу.
 *
 * Предназначен для хранения артефактов компиляции (PHP-код шаблонов, конфигов),
 * актуальность которых зависит от времени модификации источника.
 * В отличие от универсального CacheInterface, предоставляет прямой доступ
 * к пути кэш-файла для использования через include/require.
 *
 * @see FileRelatedCache
 */
interface FileRelatedCacheInterface
{
    /**
     * Путь к кэш-файлу для прямого include/require.
     *
     * Вызывающий код ОБЯЗАН сначала проверить exists().
     */
    public string $path {
        get;
    }

    /**
     * Проверяет наличие валидного кэша.
     *
     * Реализация должна учитывать TTL и актуальность исходного файла.
     * Возвращает false, если кэш отсутствует, устарел или источник изменился.
     */
    public function exists(): bool;

    /**
     * Сохраняет содержимое в кэш-файл.
     *
     * Ожидается строковое содержимое (обычно скомпилированный PHP-код).
     * Реализация должна обеспечивать атомарность записи.
     *
     * @param string $value Содержимое для записи
     */
    public function set(string $value): void;

    /**
     * Удаляет кэш-файл.
     *
     * Должен быть безопасен при отсутствии файла — не выбрасывать исключений.
     */
    public function clear(): void;
}
